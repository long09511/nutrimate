function setLanguage(lang) {
    const langData = translations[lang] || translations.vi;
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.dataset.translate;
        if (langData[key]) {
            el.innerHTML = langData[key];
        }
    });
     document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
        const key = el.dataset.translatePlaceholder;
        if (langData[key]) {
            el.placeholder = langData[key];
        }
    });
}

async function handleFormSubmit(e) {
    e.preventDefault();
    const lang = document.documentElement.lang;
    const spinner = document.getElementById('spinner');
    const btnText = document.getElementById('btn-text');
    const generateBtn = document.getElementById('generate-btn');

    spinner.classList.remove('hidden');
    btnText.textContent = translations[lang].processing || 'Processing...';
    generateBtn.disabled = true;

    try {
        await new Promise(resolve => setTimeout(resolve, 1000));

        const userInfo = getUserInput();
        if (!userInfo) {
            resetButtonState();
            return;
        }

        const calculations = performCalculations(userInfo);
        const weeklyMenu = createWeeklyMenu(userInfo);
        
        if (!weeklyMenu) {
            alert('Lỗi: Không thể tạo thực đơn. Vui lòng kiểm tra lại các lựa chọn, đặc biệt là thông tin dị ứng, hoặc thử lại sau.');
            resetButtonState();
            return;
        }

        generatedPlan = { userInfo, calculations, weeklyMenu };
        displayResults(userInfo, calculations, weeklyMenu);
        updateStreak();

    } catch (error) {
        console.error("Đã xảy ra lỗi khi tạo kế hoạch:", error);
        alert("Đã có lỗi không mong muốn xảy ra. Vui lòng thử lại. Chi tiết lỗi đã được ghi lại trong console.");
    } finally {
        resetButtonState();
    }
}

