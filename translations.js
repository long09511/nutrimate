function performCalculations({ weight, height, age, gender, activityLevel, goal, targetWeight }) {
    const lang = document.documentElement.lang;
    const langTrans = translations[lang] || translations.vi;
    const heightInMeters = height / 100;
    const bmi = weight / (heightInMeters * heightInMeters);
    let bmiCategory = '', bmiColor = '';
    if (bmi < 18.5) { bmiCategory = langTrans.bmiCat1; bmiColor = 'text-blue-400'; }
    else if (bmi < 24.9) { bmiCategory = langTrans.bmiCat2; bmiColor = 'text-green-400'; }
    else if (bmi < 29.9) { bmiCategory = langTrans.bmiCat3; bmiColor = 'text-yellow-400'; }
    else { bmiCategory = langTrans.bmiCat4; bmiColor = 'text-red-400'; }

    let bmr = (gender === 'male') ? (10 * weight + 6.25 * height - 5 * age + 5) : (10 * weight + 6.25 * height - 5 * age - 161);
    const tdee = bmr * activityLevel;

    let calorieChange = 0;
    if (goal === 'lose') calorieChange = -400;
    else if (goal === 'gain') calorieChange = 400;
    else if (goal === 'height') calorieChange = 200;

    let targetCalories = tdee + calorieChange;
    
    let timeEstimate = 'N/A';
    let goalText = '';
    if (goal === 'height') {
        timeEstimate = langTrans.goalEst1;
        goalText = langTrans.goalText1;
    } else if (goal === 'maintain') {
        timeEstimate = langTrans.goalEst2;
        goalText = langTrans.goalText2.replace('{weight}', weight);
    } else {
        const weightDifference = targetWeight - weight;
        if ((weightDifference < 0 && calorieChange < 0) || (weightDifference > 0 && calorieChange > 0)) {
            const totalCaloriesToChange = Math.abs(weightDifference) * 7700;
            const daysNeeded = totalCaloriesToChange / Math.abs(calorieChange);
            const weeksNeeded = Math.ceil(daysNeeded / 7);
            timeEstimate = weeksNeeded > 104 ? langTrans.goalText5 : langTrans.goalText4.replace('{weeks}', weeksNeeded);
        } else {
            timeEstimate = langTrans.goalEst3;
        }
        goalText = langTrans.goalText3.replace('{targetWeight}', targetWeight);
    }

    return { bmi: bmi.toFixed(1), bmiCategory, bmiColor, tdee: Math.round(tdee), targetCalories: Math.round(targetCalories), timeEstimate, goalText };
}

function getFoodSource(goal) {
    let sources = [];
    if (goal === 'maintain') {
        sources.push(database.lose, database.gain);
    } else if (goal === 'height') {
        sources.push(database.height, database.gain);
    } else {
        sources.push(database[goal]);
    }
    return sources;
}

function getAvailableDishes(sources, cuisine, type, allergies, budget) {
    let dishes = [];
    const asianCuisines = ['vietnamese', 'japanese', 'korean', 'chinese', 'thai'];
    const europeanCuisines = ['western', 'italian', 'french', 'american'];

    for (const source of sources) {
        let cuisinesToSearch = [];
        if (cuisine === 'any') {
            cuisinesToSearch = Object.keys(source);
        } else if (cuisine === 'asian') {
            cuisinesToSearch = asianCuisines;
        } else if (cuisine === 'european') {
            cuisinesToSearch = europeanCuisines;
        }

        for (const c of cuisinesToSearch) {
            if (source[c] && source[c][type]) {
                dishes.push(...source[c][type]);
            }
        }
    }
    
    const uniqueDishes = Array.from(new Set(dishes.map(d => d.nameKey))).map(nameKey => dishes.find(d => d.nameKey === nameKey));
    
    return uniqueDishes.filter(d => {
        const allergyMatch = !allergies.some(a => d.ingredients.includes(a));
        const budgetMatch = budget === 'any' || d.cost === budget;
        return allergyMatch && budgetMatch;
    });
}

function getRandomDish(dishList, usedDishes) {
    let available = dishList.filter(d => !usedDishes.includes(d.nameKey));
    if (available.length === 0) {
        usedDishes.length = 0;
        available = dishList;
    }
    if (available.length === 0) return {nameKey: "noDish", calories: 0, ingredients: []};
    const dish = available[Math.floor(Math.random() * available.length)];
    usedDishes.push(dish.nameKey);
    return dish;
}

function createWeeklyMenu({ goal, cuisineStyle, allergies, budget }) {
    const weeklyMenu = {};
    const daysOfWeek = ['Thứ Hai', 'Thứ Ba', 'Thứ Tư', 'Thứ Năm', 'Thứ Sáu', 'Thứ Bảy', 'Chủ Nhật'];
    const foodSources = getFoodSource(goal);
    let usedDishes = { breakfast: [], main: [], soup: [], fiber: [], drink: [] };

    const allBreakfasts = getAvailableDishes(foodSources, cuisineStyle, 'breakfasts', allergies, budget);
    const allMains = getAvailableDishes(foodSources, cuisineStyle, 'mains', allergies, budget);
    const allSoups = getAvailableDishes(foodSources, cuisineStyle, 'soups', allergies, budget);
    const allFibers = getAvailableDishes(foodSources, cuisineStyle, 'fibers', allergies, budget);
    const allDrinks = getAvailableDishes(foodSources, cuisineStyle, 'drinks', allergies, budget);
    
    if (allBreakfasts.length === 0 || allMains.length < 2 || allSoups.length < 2 || allFibers.length < 2) return null;

    for (const day of daysOfWeek) {
        weeklyMenu[day] = {
            breakfast: getRandomDish(allBreakfasts, usedDishes.breakfast),
            lunch: {
                main: getRandomDish(allMains, usedDishes.main),
                soup: getRandomDish(allSoups, usedDishes.soup),
                fiber: getRandomDish(allFibers, usedDishes.fiber),
            },
            dinner: {
                main: getRandomDish(allMains, usedDishes.main),
                soup: getRandomDish(allSoups, usedDishes.soup),
                fiber: getRandomDish(allFibers, usedDishes.fiber),
            },
            drink: getRandomDish(allDrinks, usedDishes.drink),
            nutritionNoteKey: nutritionNoteKeys[Math.floor(Math.random() * nutritionNoteKeys.length)],
            exerciseSuggestionKey: exerciseSuggestionKeys[Math.floor(Math.random() * exerciseSuggestionKeys.length)],
        };
    }
    return weeklyMenu;
}

